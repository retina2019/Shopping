package com.javaweb.model;

public class Product {
    public int proId;
    public String proName;
    public String cut;
    public String proRange;
    public String logistical;
    public int states;
    public String photo;
    public int introduction;
    public float orig;
    public float current;
    public String scale;
    public String quantity;
    public String warehouseTime;

    public int getProId() {
        return proId;
    }

    public void setProId(int proId) {
        this.proId = proId;
    }

    public String getProName() {
        return proName;
    }

    public void setProName(String proName) {
        this.proName = proName;
    }

    public String getCut() {
        return cut;
    }

    public void setCut(String cut) {
        this.cut = cut;
    }

    public String getProRange() {
        return proRange;
    }

    public void setProRange(String proRange) {
        this.proRange = proRange;
    }

    public String getLogistical() {
        return logistical;
    }

    public void setLogistical(String logistical) {
        this.logistical = logistical;
    }

    public int getStates() {
        return states;
    }

    public void setStates(int states) {
        this.states = states;
    }

    public String getPhoto() {
        return photo;
    }

    public void setPhoto(String photo) {
        this.photo = photo;
    }

    public int getIntroduction() {
        return introduction;
    }

    public void setIntroduction(int introduction) {
        this.introduction = introduction;
    }

    public float getOrig() {
        return orig;
    }

    public void setOrig(float orig) {
        this.orig = orig;
    }

    public float getCurrent() {
        return current;
    }

    public void setCurrent(float current) {
        this.current = current;
    }

    public String getScale() {
        return scale;
    }

    public void setScale(String scale) {
        this.scale = scale;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getWarehouseTime() {
        return warehouseTime;
    }

    public void setWarehouseTime(String warehouseTime) {
        this.warehouseTime = warehouseTime;
    }
}
